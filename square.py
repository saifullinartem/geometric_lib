def area(a):
    """
    Calculates the area of a square.

    Args:
        a (float): The length of a side of the square.

    Returns:
        float: The area of the square.
    """
    return a * a


def perimeter(a):
    """
    Calculates the perimeter of a square.

    Args:
        a (float): The length of a side of the square.

    Returns:
        float: The perimeter of the square.
    """
    return 4 * a
